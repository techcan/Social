package com.socialize.restaurant;

public class RestaurantCityModel {

	private String name;
	private String latitude;
	private String longitude;
	private String id;
	private String city_id;
	private String real_city_name;
	private String zone_id;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCity_id() {
		return city_id;
	}
	public void setCity_id(String city_id) {
		this.city_id = city_id;
	}
	public String getReal_city_name() {
		return real_city_name;
	}
	public void setReal_city_name(String real_city_name) {
		this.real_city_name = real_city_name;
	}
	public String getZone_id() {
		return zone_id;
	}
	public void setZone_id(String zone_id) {
		this.zone_id = zone_id;
	}
	
	
	
}
